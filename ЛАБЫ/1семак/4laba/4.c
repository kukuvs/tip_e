#include <stdio.h>
#include <stdlib.h>
#include <time.h>

void sortDiagonal(int A[10][10]) {
    int temp;

    for (int i = 0; i < 10; i++) {
        for (int j = 0; j < 10 - i - 1; j++) {
            if (A[j][j] > A[j + 1][j + 1]) {
                temp = A[j][j];
                A[j][j] = A[j + 1][j + 1];
                A[j + 1][j + 1] = temp;
            }
        }
    }
}

int main() {
    int A[10][10];

    srand(time(NULL)); // Инициализация генератора случайных чисел

    // Заполнение матрицы случайными значениями
    for (int i = 0; i < 10; i++) {
        for (int j = 0; j < 10; j++) {
            A[i][j] = -50 + rand() % 101; // Генерация случайных чисел от -50 до 50
        }
    }

    printf("Исходная матрица A (10x10) со случайными значениями:\n");
    // Вывод исходной матрицы
    for (int i = 0; i < 10; i++) {
        for (int j = 0; j < 10; j++) {
            printf("%4d ", A[i][j]); // Используем форматированный вывод для лучшей читаемости
        }
        printf("\n");
    }

    sortDiagonal(A); // Сортировка элементов на главной диагонали

    printf("\nМатрица A после сортировки элементов на главной диагонали:\n");
    // Вывод матрицы после сортировки элементов на главной диагонали
    for (int i = 0; i < 10; i++) {
        for (int j = 0; j < 10; j++) {
            printf("%4d ", A[i][j]); // Используем форматированный вывод для лучшей читаемости
        }
        printf("\n");
    }

    return 0;
}
